package model.persistence;

public class Point {
    private final int x;
    private final int y;

    public Point(int x, int y){
        this.x=x;
        this.y=y;
    }

    public int getX(){
        int x = this.x;
        return x;
    }

    public int getY(){
        int y = this.y;
        return y;
    }
}

